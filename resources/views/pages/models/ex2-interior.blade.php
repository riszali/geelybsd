        document.addEventListener('DOMContentLoaded', () => {
            const observerOptions = { threshold: 0.15 };
            const revealObserver = new IntersectionObserver((entries) => {
                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        entry.target.classList.add('is-revealed');
                    }
                });
            }, observerOptions);
            document.querySelectorAll('.reveal-up, .reveal-left, .reveal-right, .reveal-scale').forEach(el => revealObserver.observe(el));

            const frameInt = document.getElementById('iframe-interior');
            const nameDisplay = document.getElementById('active-interior-name');
            const swatches = document.querySelectorAll('.swatch-btn-int');

            // Logika baru untuk merefresh Iframe secara akurat
            swatches.forEach(swatch => {
                swatch.addEventListener('click', () => {
                    // Update styling tombol aktif
                    swatches.forEach(s => {
                        s.classList.remove('active', 'border-white', 'ring-1', 'ring-white/50', 'ring-offset-2', 'ring-offset-transparent');
                        s.classList.add('border-white/20');
                    });
                    swatch.classList.add('active', 'border-white', 'ring-1', 'ring-white/50', 'ring-offset-2', 'ring-offset-transparent');
                    swatch.classList.remove('border-white/20');
                    
                    // Update Nama Warna
                    nameDisplay.style.opacity = 0;
                    setTimeout(() => {
                        nameDisplay.textContent = swatch.getAttribute('data-name');
                        nameDisplay.style.color = swatch.getAttribute('data-text-color') || '#cbd5e1';
                        nameDisplay.style.opacity = 1;
                    }, 150);
                    
                    // Update Iframe dengan Cache Buster agar browser merender ulang gambarnya
                    const panoramaUrl = swatch.getAttribute('data-src');
                    const timestamp = new Date().getTime(); // Mencegah bug cache pada iframe Pannellum
                    frameInt.src = `https://cdn.pannellum.org/2.5/pannellum.htm?t=${timestamp}#panorama=${panoramaUrl}&autoLoad=true&autoRotate=-2`;
                });
            });
        });

        // Lightbox Logic
        const lightbox = document.getElementById('lightbox');
        const lightboxImg = document.getElementById('lightbox-img');
        const lightboxCaption = document.getElementById('lightbox-caption');

        function openLightbox(imgSrc, caption, isLightImage = false) {
            lightboxImg.src = imgSrc;
            lightboxCaption.textContent = caption;
            
            if(isLightImage) {
                lightboxImg.classList.add('bg-white', 'p-8');
            } else {
                lightboxImg.classList.remove('bg-white', 'p-8');
            }

            lightboxImg.classList.replace('scale-95', 'scale-100');
            lightbox.classList.remove('hidden');
            lightbox.classList.add('flex');
            
            requestAnimationFrame(() => {
                lightbox.classList.remove('opacity-0');
                lightbox.classList.add('opacity-100');
            });
            document.body.classList.add('lightbox-open');
        }

        function closeLightbox(e) {
            if (e && e.target === lightboxImg) return;
            lightboxImg.classList.replace('scale-100', 'scale-95');
            lightbox.classList.remove('opacity-100');
            lightbox.classList.add('opacity-0');
            setTimeout(() => {
                lightbox.classList.add('hidden');
                lightbox.classList.remove('flex');
                lightboxImg.src = '';
                document.body.classList.remove('lightbox-open');
            }, 300); 
        }

        document.addEventListener('keydown', function(e) {
            if (e.key === 'Escape' && !lightbox.classList.contains('hidden')) {
                closeLightbox();
            }
        });
    </script>
